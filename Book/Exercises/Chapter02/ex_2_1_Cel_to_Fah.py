##2.1 (Convert Celsius to Fahrenheit) Write a program that reads a Celsiu​s degree from the console and converts it to Fahrenheit and displays the result. 
# The formula for the conversion is as follows:
# fahrenheit = (9 / 5) * celsius + 32

celsius = (float(input("Enter the temperature in Celsius: ")))
fahrenheit = (float(celsius) * 9/5) + 32
print(str(celsius) + "°C is equal to " + str(fahrenheit) + "°F")

## or  print(f"{celsius}°C is equal to {fahrenheit}°F")

